// Helper function to get chapters by section
export const getChaptersBySection = (sectionNumber) => {
  const section = hsSectionsData.find((s) => s.sectionNumber === sectionNumber);
  return section ? section.chapters : [];
};

// Helper function to get section by chapter number
export const getSectionByChapter = (chapterNumber) => {
  return hsSectionsData.find((section) =>
    section.chapters.some((chapter) => chapter.chapterNumber === chapterNumber)
  );
};

// Helper function to get all sections
export const getAllSections = () => {
  return hsSectionsData;
};

// Default export as backup
